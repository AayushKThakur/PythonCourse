# Integer
x = 55
print(x)
print(type(x))

# Float 
y = 66.7
print(y,type(y))

# String 
name = "Aayush Thakur"
print(name, type(name))

# Boolean
adult = True
print(adult, type(adult))

# List
marks = [34, 66, 65,12,99]
print(marks, type(marks))

# Tuple
marks=(34, 55,66,77,99)
print(marks, type(marks))

# Dictionary
marks = {"aayush": 99, "kunal": 89}
print(marks, type(marks))

